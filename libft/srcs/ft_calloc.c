#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ret;

	if (count == 0 || size == 0)
	{
		count = 1;
		size = 1;
	}
	ret = ft_malloc(size, count);
	if (!ret)
		return (NULL);
	ft_bzero(ret, count * size);
	return (ret);
}
