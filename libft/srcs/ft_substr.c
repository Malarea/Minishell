#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*substr;

	if (!s)
		return (NULL);
	if (start > ft_strlen(s))
	{
		substr = ft_malloc(sizeof(char), 1);
		if (!substr)
			return (NULL);
		*substr = '\0';
		return (substr);
	}
	substr = ft_strdup(&s[start]);
	if (!substr)
		return (NULL);
	if (len < ft_strlen(substr))
		substr[len] = '\0';
	return (substr);
}
