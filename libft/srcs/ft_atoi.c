#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned long	nb;
	int				sign;
	int				i;

	if (!str)
		return (0);
	nb = 0;
	sign = 1;
	i = 0;
	while ((9 <= str[i] && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	return ((int)(sign * nb));
}
