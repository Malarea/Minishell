#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*new_str;
	int		i;

	if (!s1)
		return (NULL);
	new_str = ft_malloc(sizeof(char), ft_strlen(s1) + 1);
	if (!new_str)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new_str[i] = s1[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
